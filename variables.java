public class variables {
    public static void main(String[] args) {
        int x = 123;
        long y = 1234567890987654321L;
        byte b = 100;
        double d = 3.14;
        boolean z = false;
        char symbol = '#';
        String name = "Peter";
        System.out.println("My number is:"+x);
        System.out.println("My long number is:"+y);
        System.out.println("My byte is:"+b);
        System.out.println("My double is:"+d);
        System.out.println("My boolean is:"+z);
        System.out.println("My char is:"+symbol);
        System.out.println("My name is:"+name);
    }
}
